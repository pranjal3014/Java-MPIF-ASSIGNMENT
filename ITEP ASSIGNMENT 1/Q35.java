class SurfaceArea {
    public static void main(String args[]) {

        double radius = 7.7;
        double height = 12;
        double pi = 3.14;

        double surface_area = 2 * pi * radius * (radius + height);

        System.out.println("Surface area of the cylinder = " + surface_area + " cm\u00B2");
    }
}

